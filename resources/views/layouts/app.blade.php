<script>
    $(document).on("click", ".post-like", function(){
       var id = $(this).data("id");

       $.ajax({
           type    :   "POST",
           url     :   "{{ url('/postlike') }}",
           data    :   "_token={{csrf_token()}}&id="+id,
           success :   function(data){

            if(data.message=='success' && data.liked==1){
                $('.icon_'+id).remove();
                $('#post_like_'+id).append('<i class="fa-solid fa-heart icon_'+id+'"> '+data.data+'</i>');
            }else if(data.message=='success' && data.liked==0){

                $('.icon_'+id).remove();
                $('#post_like_'+id).append('<i class="fa-regular fa-heart icon_'+id+'"> '+data.data+'</i>');

            }

           }
       });
   });
</script>
